public class CetakAngkaGenap {
    public static void main(String[] args) {
        // Batas maksimum angka genap yang ingin dicetak
        int batas = 10;

        // Cetak angka genap dari 2 hingga batas
        for (int i = 2; i <= batas; i += 2) {
            System.out.println(i);
        }
    }
}

    

