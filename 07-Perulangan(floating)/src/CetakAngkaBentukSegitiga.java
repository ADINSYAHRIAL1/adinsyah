public class CetakAngkaBentukSegitiga {
    public static void main(String[] args) {
        // Ukuran segitiga
        int tinggiSegitiga = 5;

        // Cetak segitiga
        for (int i = 1; i <= tinggiSegitiga; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}

    

