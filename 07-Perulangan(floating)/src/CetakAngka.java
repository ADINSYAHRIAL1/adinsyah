public class CetakAngka {
    public static void main(String[] args) {
        // Cetak angka 1 sampai 5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        
        // Cetak angka 5 sampai 1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}

    

